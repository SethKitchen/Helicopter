//! `final-report` subcommand: the project's capstone. Runs ONE integrated solve and
//! writes a consolidated `build_output/FINAL_REPORT.md` + the STEP/STL/DXF files.
//!
//! "Integrated" means a single optimization: the **continuous** Nelder–Mead simplex
//! ([`helisim_design::optimize_design`]) finds the geometry that minimizes gross mass
//! while the weight closure folds in the mission energy, the 10-year/365-per-year
//! service life (shallow-DoD pack oversize) and the envelope — so the recommended
//! design is the TRUE optimum (located between grid nodes, flagged if it rests on a
//! physical bound), not a grid corner. Also reports the human-scale version, the
//! swashplate/rotor-head control system, and confirms the 1:1 charge.

use std::fmt::Write as _;
use std::fs;

